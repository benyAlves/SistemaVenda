    private void txtObsKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtObsKeyReleased
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            btnGuardar.requestFocus();
            if (isSuprimento) {
                suprimento();
            } else {
                sangria();
            }
        }
    }//GEN-LAST:event_txtObsKeyReleased

