     private void sangria() {
        try {
            double valor = Util.convertStringEmDouble(txtValor.getText());
            if (valor > 0.00) {
                Transacao t = new Transacao(CaixaPane.caixa, MainView.utilizadorLogado, new Date(), valor, 0.0, "Sangria de Caixa", "não", valor);
                t.setHora(new Date());
                t.setObservacao(txtObs.getText());
                if (transControle.guardar(t) != null) {
                    JOptionPane.showMessageDialog(rootPane, "Sangria Efectuada!", "Informação", JOptionPane.INFORMATION_MESSAGE, Icone.INFORMACAO);
                    CaixaPane.actualizarTabela();
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Ocorreu um erro ao efectuar a Sangria", "Erro", JOptionPane.ERROR_MESSAGE, Icone.ERRO);
                }
            } else {
                JOptionPane.showMessageDialog(rootPane, "Informe o valor", "Erro", 1, Icone.INFORMACAO);
            }

        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(rootPane, "Informe um valor válido\n" + ex, "Informação", JOptionPane.INFORMATION_MESSAGE, Icone.INFORMACAO);
        }
    }


    private void suprimento() {
        try {
            double valor = Util.convertStringEmDouble(txtValor.getText());
            if (valor > 0) {
                Transacao t = new Transacao(CaixaPane.caixa, MainView.utilizadorLogado, new Date(), valor, 0.0, "Suprimento de Caixa", "não", valor);
                t.setHora(new Date());
                t.setObservacao(txtObs.getText());
                if (transControle.guardar(t) != null) {
                    JOptionPane.showMessageDialog(rootPane, "Suprimento Efectuada!", "Informação", JOptionPane.INFORMATION_MESSAGE, Icone.INFORMACAO);
                    CaixaPane.actualizarTabela();
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Ocorreu um erro ao efectuar o Suprimento", "Erro", JOptionPane.ERROR_MESSAGE, Icone.ERRO);
                }

            } else {
                JOptionPane.showMessageDialog(rootPane, "Informe o valor", "Informação", 1, Icone.INFORMACAO);
            }
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(rootPane, "Informe um valor válido\n" + ex, "Informação", JOptionPane.INFORMATION_MESSAGE, Icone.INFORMACAO);
        }
    }

