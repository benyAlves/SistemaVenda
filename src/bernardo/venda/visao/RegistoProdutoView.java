    private boolean estaPreenchido() {
        if (txtNomeDescricao.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Informe o nome do produto");
            txtNomeDescricao.requestFocus();
            return false;
        }

        if (txtEstoqueMin.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Informe o estoque minimo");
            txtNomeDescricao.requestFocus();
            return false;
        }

        if (txtQActual.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Informe o estoque actual");
            txtQActual.requestFocus();
            return false;
        }

        if (txtVCompra.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Informe o preço do produto");
            txtVCompra.requestFocus();
            return false;
        }

        if (txtValorVenda.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Informe o custo do produto");
            txtValorVenda.requestFocus();
            return false;
        }
        return true;
    }

    private void actualizarProduto(Produto p) {
        if (estaPreenchido()) {
            try {
                p.setCategoria(categoria);
                p.setFornecedor(fornecedor);
                p.setQuantidadeMax(Util.convertStringEmInteiro(txtEstoqueMax.getText()));
                p.setCodigoBarras(txtCodBarras.getText());
                p.setDescricao(txtNomeDescricao.getText());
                p.setObservacao(txtObservacao.getText());
                p.setPrecoCompra(Util.convertStringEmDouble(txtVCompra.getText()));
                p.setPrecoVenda(Util.convertStringEmDouble(txtValorVenda.getText()));
                p.setQuantidadeActual(Util.convertStringEmInteiro(txtQActual.getText()));
                p.setQuantidadeMin(Util.convertStringEmInteiro(txtEstoqueMin.getText()));
                p.setFoto(converteImagem());
                if (jComboBox1.getSelectedItem() != null)
                    p.setUnidadeMedida(jComboBox1.getSelectedItem().toString());
                if (cp.atualizar(p) != null) {
                    JOptionPane.showMessageDialog(rootPane, "Produto actualizado!", "Informação", 1, Icone.INFORMACAO);
                    produto = null;
                    ProdutoView.actualizarTabelaProduto();
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "Ocorreu um erro ao actualizar", "Erro", JOptionPane.ERROR_MESSAGE, Icone.ERRO);
                }

            } catch (ParseException ex) {
                JOptionPane.showMessageDialog(this, "Verifique os dados informados", "Erro", JOptionPane.ERROR_MESSAGE, Icone.ERRO);
            }
        }
    }

    private void calculaLucro() {
        try {
            if (!txtValorVenda.getText().trim().isEmpty() && !txtVCompra.getText().trim().isEmpty()) {
                double preco = Util.convertStringEmDouble(txtValorVenda.getText());
                double custo = Util.convertStringEmDouble(txtVCompra.getText());
                if (preco > custo) {
                    lbLucro.setForeground(new Color(41, 127, 1));
                    lbLucro.setText(((preco - custo) + "").replace(".", ","));
                } else {
                    lbLucro.setForeground(new Color(228, 9, 14));
                    lbLucro.setText(((preco - custo) + "").replace(".", ","));
                }
            }
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(this, "Verifique o preço e o custo", "Erro", JOptionPane.ERROR_MESSAGE, Icone.ERRO);
        }


    }

