   private void guardarProduto() {

        String codigoBarras = null;
        String nome = null;
        String observacao = null;
        double precoCompra = 0;
        double precoVenda = 0;
        int quantidadeMin = 0;
        Integer quantidadeMax = null;
        int quantidadeActual = 0;

        if (estaPreenchido()) {
            try {
                codigoBarras = txtCodBarras.getText();
                nome = txtNomeDescricao.getText();
                observacao = txtObservacao.getText();
                quantidadeMin = Util.convertStringEmInteiro(txtEstoqueMin.getText());
                quantidadeMax = Util.convertStringEmInteiro(txtEstoqueMax.getText());
                quantidadeActual = Util.convertStringEmInteiro(txtQActual.getText());
                precoVenda = Util.convertStringEmDouble(txtValorVenda.getText());
                precoCompra = Util.convertStringEmDouble(txtVCompra.getText());
                produto = new Produto(categoria, nome, precoCompra, precoVenda, quantidadeMin, quantidadeActual);
                produto.setCodigoBarras(codigoBarras);
                produto.setObservacao(observacao);
                produto.setFornecedor(this.fornecedor);
                produto.setQuantidadeMax(quantidadeMax);
                produto.setFoto(converteImagem());
                if (jComboBox1.getSelectedItem() != null)
                    produto.setUnidadeMedida(jComboBox1.getSelectedItem().toString());

                if (cp.guardar(produto) != null) {
                    JOptionPane.showMessageDialog(rootPane, "Produto Registado!", "Informação", 1, Icone.INFORMACAO);
                    ProdutoView.actualizarTabelaProduto();
                    limparCampos();
                    produto = null;
                } else {
                    JOptionPane.showMessageDialog(this, "Ocorreu um erro ao guardar", "Erro", JOptionPane.ERROR_MESSAGE, Icone.ERRO);
                }

            } catch (ParseException ex) {
                JOptionPane.showMessageDialog(rootPane, "Verifique os dados informados", "Erro de dados", JOptionPane.ERROR_MESSAGE, Icone.ERRO);
            }
        }
    }

