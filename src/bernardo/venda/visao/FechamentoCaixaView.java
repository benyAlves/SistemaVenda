    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        caixa.setAberto(false);
        caixa.setDataFechamento(new Date());
        caixa.setObservacao(txtObs.getText());
        caixa.setValorInformado(valorInformado);
        caixa.setValorFinal(saldoFinal);
        caixa.setValorRetirado(saldoRetirado);
        caixa.setValorAdicionado(saldoAdicionado);
        if (caixaControle.atualizar(caixa) != null) {
            JOptionPane.showMessageDialog(rootPane, "O Caixa foi fechado!", "Fechamento de Caixa", JOptionPane.INFORMATION_MESSAGE);
            CaixaPane.caixa = caixaControle.findStateUser(true, MainView.utilizadorLogado);
            CaixaPane.actualizarCaixa();
            CaixaPane.actualizarTabela();
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(rootPane, "Ocorreu um erro ao fechar o Caixa", "Fechamento de Caixa", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

   public void setCaixa(List<Transacao> lista, Caixa c) {
        this.caixa = c;
        for (int i = 0; i < lista.size(); i++) {
            Transacao t = lista.get(i);
            if (!t.getCancelado().equals("Sim") && t.getTipo().equalsIgnoreCase("Venda")) {
                this.saldoCaixa += t.getValorFinal();
            } else if (!t.getCancelado().equals("Sim") && t.getTipo().equalsIgnoreCase("Sangria de Caixa")) {
                this.saldoRetirado += t.getValorFinal();
            } else if (!t.getCancelado().equals("Sim") && t.getTipo().equalsIgnoreCase("Suprimento de Caixa")) {
                this.saldoAdicionado += t.getValorFinal();
            }
        }
        this.saldoFinal = caixa.getValorInicial() + this.saldoCaixa + this.saldoAdicionado - this.saldoRetirado;
        this.quebra = this.saldoFinal;
        txtSaldoFinal.setText(Util.formatarMoeda(saldoFinal));
        txtQuebra.setText("-" + Util.formatarMoeda(quebra));
    }

    private void calcularQuebra(double valor) {
        if ((valor - saldoFinal) < 0) {
            txtQuebra.setText("Quebra de Caixa(Valor em Falta):");
            txtQuebra.setForeground(new Color(220, 15, 22));
            lbQuebra.setForeground(new Color(220, 15, 22));
        } else if ((valor - saldoFinal) < 0) {
            txtQuebra.setText("Quebra de Caixa(Valor em Sobra):");
            txtQuebra.setForeground(Color.BLACK);
            lbQuebra.setForeground(Color.BLACK);
        } else {
            txtQuebra.setText("Quebra de Caixa                :");
            txtQuebra.setForeground(Color.BLACK);
            lbQuebra.setForeground(Color.BLACK);
        }
        txtQuebra.setText(Util.formatarMoeda(valor - saldoFinal));
        this.quebra = valor - caixa.getValorFinal();
    }
