    private void txtValorEmCaixaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtValorEmCaixaKeyReleased
        try {
            this.valorInformado = Util.convertStringEmDouble(txtValorEmCaixa.getText());
            calcularQuebra(valorInformado);
        } catch (ParseException ex) {
            if (!txtValorEmCaixa.getText().trim().isEmpty())
            JOptionPane.showMessageDialog(rootPane, "Valor informado é inválido\nVerifique por favor");
            calcularQuebra(0.0);
        }
    }//GEN-LAST:event_txtValorEmCaixaKeyReleased

