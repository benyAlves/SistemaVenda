   private void guardaUtilizador() {
        if (isPreenchido()) {
            Utilizador u = new Utilizador(txtNome.getText(), txtUtilizador.getText(), new String(txtSenha.getPassword()), cmbxPerfil.getSelectedItem().toString());
            if (utilControle.guardar(u) != null) {
                JOptionPane.showMessageDialog(rootPane, "Utilizador registado!");
                limparCampos();
                actualizarTabela();
            } else {
                JOptionPane.showMessageDialog(rootPane, "Ocorreu um erro ao registar", "Erro de registo", JOptionPane.ERROR_MESSAGE);
            }
        }
    }

    private boolean isPreenchido() {
        if (txtNome.getText().trim().length() < 0) {
            JOptionPane.showMessageDialog(rootPane, "Preencha o campo nome");
            txtNome.requestFocus();
            return false;
        } else if (txtUtilizador.getText().trim().length() < 0) {
            JOptionPane.showMessageDialog(rootPane, "Preencha o campo utilizador");
            txtUtilizador.requestFocus();
            return false;
        }
        if (new String(txtSenha.getPassword()).trim().length() < 0) {
            JOptionPane.showMessageDialog(rootPane, "Preencha o campo nome");
            txtSenha.requestFocus();
            return false;
        }
        return true;
    }

    private void actualizarUtilizador(Utilizador u) {
        if (isPreenchido()) {
            u.setNomeCompleto(txtNome.getText());
            u.setNomeUtilizador(txtUtilizador.getText());
            u.setSenha(new String(txtSenha.getPassword()));
            u.setPerfil(cmbxPerfil.getSelectedItem().toString());
            if (utilControle.atualizar(u) != null) {
                JOptionPane.showMessageDialog(rootPane, "Utilizador actualizado!");
                limparCampos();
                btnActualizar.setEnabled(false);
                btnRemover.setEnabled(false);
                btnGuardar.setEnabled(true);
                actualizarTabela();
            } else {
                JOptionPane.showMessageDialog(rootPane, "Ocorreu um erro ao actualizar", "Erro de actualização", JOptionPane.ERROR_MESSAGE);
            }
        }
    }

    private void removerUtilizador(Utilizador utilizador) {
        if (utilControle.remover(utilizador)) {
            JOptionPane.showMessageDialog(rootPane, "Utilizador removido!");
            btnActualizar.setEnabled(false);
            btnRemover.setEnabled(false);
            btnGuardar.setEnabled(true);
            actualizarTabela(utilizador);
            limparCampos();
        } else {
            JOptionPane.showMessageDialog(rootPane, "Ocorreu um erro ao remover", "Erro de remoção", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void actualizarTabela() {
        this.lista = utilControle.findAll();
        tabelaUtilizador.setModel(new TabelaUtilizador(lista));
    }

    private void actualizarTabela(Utilizador utilizador) {
        this.lista.remove(utilizador);
        tabelaUtilizador.setModel(new TabelaUtilizador(lista));
    }

    private void preencheCampos(Utilizador utilizador) {
        txtNome.setText(utilizador.getNomeCompleto());
        txtUtilizador.setText(utilizador.getNomeUtilizador());
        txtSenha.setText(utilizador.getSenha());
        cmbxPerfil.setSelectedItem(utilizador.getPerfil());
    }

    private void limparCampos() {
        txtNome.setText(null);
        txtUtilizador.setText(null);
        txtSenha.setText(null);
        cmbxPerfil.setSelectedIndex(0);
        tabelaUtilizador.clearSelection();
    }
    
