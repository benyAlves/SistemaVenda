    private void tabelaUtilizadorMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaUtilizadorMouseClicked
        int linha = tabelaUtilizador.getSelectedRow();
        if (linha != -1) {
            this.utilizador = TabelaUtilizador.getUtilizadorAt(linha);
            preencheCampos(this.utilizador);
            btnRemover.setEnabled(true);
            btnActualizar.setEnabled(true);
            btnGuardar.setEnabled(false);
        }
    }//GEN-LAST:event_tabelaUtilizadorMouseClicked

